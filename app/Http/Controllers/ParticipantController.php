     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validateData = $request->validate([
            'user_id' => 'required',
            'event_id' => 'required'
        ]);

        if (Participant::create($validateData)) return response()->json(['message' => 'Success add participant']);

        return response()->json(['message' => 'Failed add participant'], 402);
    }

